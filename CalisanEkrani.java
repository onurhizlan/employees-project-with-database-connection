    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String ad = jTextField2.getText();
       String soyad = jTextField3.getText();
       String departman = jTextField4.getText();
       String maas = jTextField5.getText(); 
       
       int secilen_alan = jTable1.getSelectedRow();
        if (secilen_alan == -1) {
            if (model.getRowCount() == 0) {
                jLabel5.setText("Çalışanlar tablosu bos");
            }else{
                jLabel5.setText("Lutfen guncellenecek calısan seciniz");
            }
        }else{
            int id = (int) model.getValueAt(secilen_alan, 0);
            baglanti.calisanGuncelle(id, ad, soyad, departman, maas);
            calisanGoruntule();
            jLabel5.setText("Çalışan guncellendi");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

