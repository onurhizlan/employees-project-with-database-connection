    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jLabel5.setText("");
        int secilen_alan = jTable1.getSelectedRow();
        if (secilen_alan == -1) {
            if (secilen_alan == 0) {
                jLabel5.setText("Çalışan tablosu suanda bos");
            }else{
                jLabel5.setText("Lütfen silinecek calısan seciniz");
            }
        }else{
            int id = (int)model.getValueAt(secilen_alan, 0);
            baglanti.calisanSil(id);
            calisanGoruntule();
            jLabel5.setText("Çalışan başarıyla silindi");
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    
    public void dinamikAra(String ara){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        jTable1.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(ara));
        
    }
    /**
     * @param args the command line arguments
     */
    
    public void calisanGoruntule(){
        model.setRowCount(0); //tablomuz her seferinde bosalıcak daha sonra tekrar dolucak nedeni ise hata alabiliriz die
        ArrayList<Calisan> calisanlar = new ArrayList<Calisan>();
        calisanlar = baglanti.calisanlariGetir();
        if (calisanlar != null) {
            for(Calisan calisan : calisanlar){
                Object[] eklenecek = {calisan.getId(), calisan.getAd(), calisan.getSoyad(), calisan.getDepartman(), calisan.getMaas()};
                
                model.addRow(eklenecek);
            }
        }
    }
    
